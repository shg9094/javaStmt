public class Gugudan {
	

	public static void main(String[] args) {
		System.out.println("2단부터  9단까지 구구단 출력");
			
		
			for (int i = 2; i <= 2; i++) {
				for (int j = 1; j <= 9; j++) {
					System.out.println(i+"*"+j+"="+i*j+"\t"
									+ (i+1)+"*"+j+"="+(i+1)*j+"\t"
									+ (i+2)+"*"+j+"="+(i+2)*j+"\t"
									+ (i+3)+"*"+j+"="+(i+3)*j+"\t");
					}
				} 
				System.out.println();
				
			for (int i = 6; i <= 6; i++) {
				for (int j = 1; j <= 9; j++) {
					System.out.println(i+"*"+j+"="+i*j+"\t"
							 + (i+1)+"*"+j+"="+(i+1)*j+"\t"
							 + (i+2)+"*"+j+"="+(i+2)*j+"\t"
							 + (i+3)+"*"+j+"="+(i+3)*j+"\t");
					
				}
			}
			}
	}


